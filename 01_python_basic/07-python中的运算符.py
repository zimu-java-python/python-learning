# 算数（数学）运算符
print("1 + 1 =", 1 + 1)
print("2 - 1 =", 2 - 1)
print("3 * 3 =", 3 * 3)
print("4 / 2 =", 4 / 2)
print("11 // 2 =", 11 // 2)
print("9 % 2 =", 9 % 2)
print("2 ** 2 =", 2 ** 2)

# 赋值运算符
num = 1 +2 * 3
print("num =", num)

